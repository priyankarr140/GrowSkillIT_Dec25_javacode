package day2;
public class Program4_ElseIf {
	public static void main(String[] args) {
		int age=18;
		if(age==18)
		{
			System.out.println("Congrats on your first vote");
		}
		else if(age>=18)
		{
			System.out.println("You can vote");
		}
		else
		{
			System.out.println("You cannot vote");
		}

	}

}
